package com.miri.materialdesign.demo.model;

/**
 * 〈一句话功能简述〉〈功能详细描述〉
 *
 * @author PengL, , 2015/11/30.
 */
public class Person {

    String name;

    int age;

    public Person() {
    }

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
